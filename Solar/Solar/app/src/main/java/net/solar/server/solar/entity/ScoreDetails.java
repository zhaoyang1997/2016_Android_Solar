package net.solar.server.solar.entity;

public class ScoreDetails {

    private String name;
    private int count;

    public ScoreDetails() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
